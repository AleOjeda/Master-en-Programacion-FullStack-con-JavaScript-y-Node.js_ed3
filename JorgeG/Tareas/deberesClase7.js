// Ejercicio 2

// Usando for

for (var i = 0; i < 100; i++) {
    console.log(i);
}

// Usando while y break

var i = 1;
while (i <= 1000) {
    console.log(i);
if (i === 100) 
    	break;
    i++;
    }

// Usando do..while

var i = 0;
do {
   i++;
   console.log(i);
} while (i < 100);

